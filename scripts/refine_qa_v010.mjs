function cleanLabel(value) {
  return String(value || '')
    .replace(/^\s*(?:\d+[.、．]\d+|\d+[.、．]|[一二三四五六七八九十]+[、.．])\s*/u, '')
    .replace(/^第\d+步[:：]\s*/u, '')
    .replace(/\b\d+[.、．]\d+\s*/g, '')
    .replace(/（原文清洁版\s*v\d+）/g, '')
    .replace(/原文清洁版\s*v\d+/g, '')
    .replace(/\s+\/\s+/g, ' / ')
    .replace(/\s+/g, ' ')
    .trim();
}

function cleanQuestion(question) {
  return cleanLabel(question)
    .replace(/^Q[:：]\s*(.+?)的(?:\d+[.、．]\s*)?(.+?)是什么[？?]$/, '$1时，$2怎么做？')
    .replace(/^Q[:：]\s*(.+?)的(.+?)是什么[？?]$/, '$1时，$2是什么？')
    .replace(/^Q[:：]\s*/, '')
    .replace(/的(?:\d+[.、．]\s*)?(.+?)是什么[？?]$/, '的$1是什么？')
    .replace(/^(.+?)中(.+是什么[？?])$/, '$1$2')
    .replace(/^(.+?)有哪些关键要点[？?]$/, '$1有哪些关键要点？')
    .replace(/的课程名称是什么[？?]$/, '是什么课？')
    .replace(/的核心定位是什么[？?]$/, '适合什么学生？')
    .replace(/的教学目标是什么[？?]$/, '主要培养什么能力？')
    .replace(/的课程模式是什么[？?]$/, '怎么上课？')
    .replace(/的教学重点是什么[？?]$/, '重点学什么？')
    .replace(/“(.+?)”在哪个小学数学课本目录里[？?]$/, '$1在哪个小学数学年级学？')
    .replace(/“(.+?)”在哪个初中数学课本目录里[？?]$/, '$1在哪个初中数学年级学？')
    .replace(/[?]$/, '？');
}

function cleanAnswer(answer) {
  return String(answer || '')
    .replace(/；{2,}/g, '；')
    .replace(/\s+/g, ' ')
    .replace(/；\s*；/g, '；')
    .trim();
}

